package com.mgm.main;

import java.io.File;

/**
 *
 * @author Usuario14
 */
public class Persona {
    private String name;
    private int edad;
    
    public Persona() {
        
    }
    
    public Persona(String name, int edad) {
        this.name = name;
        this.edad = edad;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
    
    @Override
    public String toString() {
        
        return name + "," + edad; 
    }
}
